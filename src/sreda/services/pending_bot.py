_CB_PREFIX = "pb:"


def is_pending_callback(callback_data: str) -> bool:
    """True если callback_data относится к pending-боту.

    Сохранён для backwards-compat: при апгрейде с прошлой версии
    в БД могут остаться сообщения с устаревшими `pb:<branch>`
    callback_data. Webhook видит их как pending-callback и зовёт
    `match()`, который теперь всегда отдаёт welcome.
    """
    return callback_data.startswith(_CB_PREFIX)


def match(input_text: str | None, *, is_callback: bool = False) -> PendingReply:
    """Возвращает единое приветствие на любой ввод pending-юзера.

    Раньше принимала text/callback и роутила в одну из 7 веток. С
    2026-04-27 веток нет — для всего pending-периода один и тот же
    текст. Аргументы оставлены для совместимости с webhook'ом.
    """
    _ = (input_text, is_callback)  # пометить как намеренно неиспользуемые
    return _WELCOME


def build_inline_keyboard(reply: PendingReply) -> dict | None:
    """Telegram inline_keyboard. С 2026-04-27 всегда возвращает None —
    кнопок в pending-фазе нет. Сигнатура сохранена для совместимости
    с вызывающим кодом в `telegram_webhook`."""
    if not reply.buttons:
        return None
    return {
        "inline_keyboard": [
            [{"text": label, "callback_data": f"{_CB_PREFIX}{branch}"}]
            for label, branch in reply.buttons
        ],
    }
